package org.jowidgets.message.impl.p2p.simple;

import java.io.Serializable;

final class Peer implements Serializable {

	private static final long serialVersionUID = -3364843952184150308L;

	private final String host;
	private final int port;

	Peer(final String host, final int port) {
		this.host = host;
		this.port = port;
	}

	String getHost() {
		return host;
	}

	int getPort() {
		return port;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((host == null) ? 0 : host.hashCode());
		result = prime * result + port;
		return result;
	}

	@Override
	public boolean equals(final Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		final Peer other = (Peer) obj;
		if (host == null) {
			if (other.host != null) {
				return false;
			}
		}
		else if (!host.equals(other.host)) {
			return false;
		}
		if (port != other.port) {
			return false;
		}
		return true;
	}

}
